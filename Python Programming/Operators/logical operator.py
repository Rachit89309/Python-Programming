a , b = 5, 4
print(a > 4 & b < 10)     # use to check the conditions
print(a > 4 and b < 3)   # and operator is applicable only when both the condition are true
# if one condition will be false it will give false output
print(a > 4 or b < 13)    # in case of or one condition must be true

# not operator -> reverse the result
print(not(a))

c = True
print(not(c))
print(a <= 5 and c)
