# assignment operator- used to assign values to variables





a = 5
a += 2
print(a)

# more than one assignment in a line
g, h, i = 5, 8, 9
print(g, h, i)
c, d = 4, 3
e = c + d
c += 2
e += c    # c = c + a
print(e)

# comparison operator - compares the value either return true of false according to the condition
f = 5
print(f == 5)
print(f <= 5)
print(f < 5)
print( f != 5)