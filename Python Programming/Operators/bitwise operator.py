a = 5
b = 4
print(a & b)   # and operator
print(a | b)   # or operator
print(a ^ b)    #when both bits are same it will give 0 and if both are different it will give 1
print(~a)      #  ~a = -(a + 1)    complement operator
# left shift
print(a<< 2)   # if x << n  then x * (2**n)
# right shift
print(a>> 2)
#  in this case we loose the bits 
# x >> n  then x//(2**n)
y  = 5//(2**2)
print(y)



